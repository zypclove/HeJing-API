using ERP.HostApp.Services;
using ERP.Shared.DTO.PurchaseRequestItem;

namespace ERP.HostApp.Controllers;

/// <summary>
/// 采购需求清单
/// </summary>
[Area("Erp")]
public class PurchaseRequestItemController : AppControllerBase
{
    private readonly PurchaseRequestItemService _service;

    /// <summary>
    /// 构造函数
    /// </summary>
    /// <param name="serviceProvider"></param>
    /// <param name="service"></param>
    public PurchaseRequestItemController(IServiceProvider serviceProvider, PurchaseRequestItemService service) :
        base(serviceProvider)
    {
        _service = service;
    }

    /// <summary>
    /// 新增
    /// </summary>
    /// <param name="input"></param>
    /// <returns></returns>
    [HttpPost]
    public async Task<ApiResult<Guid>> Create(PurchaseRequestItemModel input)
    {
        var result = await _service.Create(input);
        return Success(result);
    }

    /// <summary>
    /// 更新
    /// </summary>
    /// <param name="input"></param>
    /// <returns></returns>
    [HttpPost]
    public async Task<ApiResult<bool>> Update(PurchaseRequestItemUpdateInDto input)
    {
        var result = await _service.Update(input);
        return Success(result);
    }

    /// <summary>
    /// 删除
    /// </summary>
    /// <param name="input"></param>
    /// <returns></returns>
    [HttpPost]
    public async Task<ApiResult<bool>> Delete(PurchaseRequestItemDeleteInDto input)
    {
        var result = await _service.Delete(input);
        return Success(result);
    }

    /// <summary>
    /// 批量删除
    /// </summary>
    /// <param name="input"></param>
    /// <returns></returns>
    [HttpPost]
    public async Task<ApiResult<bool>> BatchDelete(PurchaseRequestItemBatchDeleteInDto input)
    {
        var result = await _service.BatchDelete(input);
        return Success(result);
    }

    /// <summary>
    /// 获取清单
    /// </summary>
    /// <param name="input"></param>
    /// <returns></returns>
    [HttpGet]
    public async Task<ApiResult<PagingOut<PurchaseRequestItemQueryOutDto>>> Query([FromQuery] PurchaseRequestItemQueryInDto input)
    {
        var result = await _service.Query(input);
        return Success(result);
    }

    /// <summary>
    /// 获取详情
    /// </summary>
    /// <param name="input"></param>
    /// <returns></returns>
    [HttpGet]
    public async Task<ApiResult<PurchaseRequestItemGetOutDto>> Get([FromQuery] PurchaseRequestItemGetInDto input)
    {
        var result = await _service.Get(input);
        return Success(result);
    }
}