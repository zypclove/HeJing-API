using ERP.HostApp.Services;
using ERP.Shared.DTO.ReturnGoodsItem;

namespace ERP.HostApp.Controllers;

/// <summary>
/// 退货清单
/// </summary>
[Area("Erp")]
public class ReturnGoodsItemController : AppControllerBase
{
    private readonly ReturnGoodsItemService _service;

    /// <summary>
    /// 构造函数
    /// </summary>
    /// <param name="serviceProvider"></param>
    /// <param name="service"></param>
    public ReturnGoodsItemController(IServiceProvider serviceProvider, ReturnGoodsItemService service) :
        base(serviceProvider)
    {
        _service = service;
    }

    /// <summary>
    /// 新增
    /// </summary>
    /// <param name="input"></param>
    /// <returns></returns>
    [HttpPost]
    public async Task<ApiResult<Guid>> Create(ReturnGoodsItemCreateInDto input)
    {
        var result = await _service.Create(input);
        return Success(result);
    }

    /// <summary>
    /// 更新
    /// </summary>
    /// <param name="input"></param>
    /// <returns></returns>
    [HttpPost]
    public async Task<ApiResult<bool>> Update(ReturnGoodsItemUpdateInDto input)
    {
        var result = await _service.Update(input);
        return Success(result);
    }

    /// <summary>
    /// 删除
    /// </summary>
    /// <param name="input"></param>
    /// <returns></returns>
    [HttpPost]
    public async Task<ApiResult<bool>> Delete(ReturnGoodsItemDeleteInDto input)
    {
        var result = await _service.Delete(input);
        return Success(result);
    }

    /// <summary>
    /// 批量删除
    /// </summary>
    /// <param name="input"></param>
    /// <returns></returns>
    [HttpPost]
    public async Task<ApiResult<bool>> BatchDelete(ReturnGoodsItemBatchDeleteInDto input)
    {
        var result = await _service.BatchDelete(input);
        return Success(result);
    }

    /// <summary>
    /// 获取清单
    /// </summary>
    /// <param name="input"></param>
    /// <returns></returns>
    [HttpGet]
    public async Task<ApiResult<PagingOut<ReturnGoodsItemQueryOutDto>>> Query([FromQuery] ReturnGoodsItemQueryInDto input)
    {
        var result = await _service.Query(input);
        return Success(result);
    }

    /// <summary>
    /// 获取详情
    /// </summary>
    /// <param name="input"></param>
    /// <returns></returns>
    [HttpGet]
    public async Task<ApiResult<ReturnGoodsItemGetOutDto>> Get([FromQuery] ReturnGoodsItemGetInDto input)
    {
        var result = await _service.Get(input);
        return Success(result);
    }
}