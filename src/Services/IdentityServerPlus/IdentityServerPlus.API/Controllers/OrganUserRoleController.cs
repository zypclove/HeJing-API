using IdentityServerPlus.HostApp.Services;
using IdentityServerPlus.Shared.DTO.OrganUserRole;

namespace IdentityServerPlus.HostApp.Controllers;

/// <summary>
/// 用户角色
/// </summary>
[Area("isp")]
public class OrganUserRoleController : AppControllerBase
{
    private readonly OrganUserRoleService _service;

    /// <summary>
    /// 构造函数
    /// </summary>
    /// <param name="serviceProvider"></param>
    /// <param name="service"></param>
    public OrganUserRoleController(IServiceProvider serviceProvider, OrganUserRoleService service) :
